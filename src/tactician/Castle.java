package tactician;

/**
 * This enum lists the two types of castling allowed in chess. Kingside is with the rook on the h1
 * or h8 squares; queenside is with the rook on the a1 or a8 squares.
 * 
 * @see <a href="https://en.wikipedia.org/wiki/Castling">Castling</a>
 * @author Phil Leszczynski
 */
public enum Castle {
	KINGSIDE,
	QUEENSIDE;
}
